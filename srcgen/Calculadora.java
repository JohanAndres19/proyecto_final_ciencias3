public class Calculadora {
      private int x;
      private int y;

      public void sumar(){
        System.out.println(x + y + 0);
      }

      public void restar(){
        System.out.println(x - y - 0);
      }

      public void multiplicar(){
        System.out.println(x * y * 1);
      }

      public void division(){
        System.out.println(x / y / 1);
      }
}